# Задано три числа. Якщо хоч одне з них додатне, то знайти квадрат суми. В
# іншому випадку – суму квадратів.

num_1 = int ( input ( "Введіть значення num_1: " ) )
num_2 = int ( input ( "Введіть значення num_2: " ) )
num_3 = int ( input ( "Введіть значення num_3: " ) )

if num_1 >= 0 or num_2 >= 0 or num_3 >= 0 :
    print ( (num_1 + num_2 + num_3) ** 2 )
else :
    print ( (num_1 ** 2) + (num_2 ** 2) + (num_3 ** 2) )
